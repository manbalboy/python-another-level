# 1. 비교연산
print(2 > 3)  # False
print(15 < 30)  # True
print(1.5 >= 0)  # True
print(3 <= 3)  # True
print("팙팙팙" == "팙팙팙")  # True
print("팙팙팙1" != "팙팙팙", end="\n=============== \n")  # True

# 2. 논리연산
print(4 < 6 and 10 >= 10)
print(4 > 6 or 10 >= 10)
print(not 10 >= 10, end="\n=============== \n")

# 3. 멤버십 연산
print("a" in "abc")
print("d" not in "abc")
